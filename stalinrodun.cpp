struct Tree {
	typedef int T;
	static constexpr T unit = INT_MIN;
	T f(T a, T b) { return max(a, b); } // (any associative fn)
	vector<T> s; int n;
	Tree(int n = 0, T def = unit) : s(2*n, def), n(n) {}
	void update(int pos, T val) {
		for (s[pos += n] = val; pos /= 2;)
			s[pos] = f(s[pos * 2], s[pos * 2 + 1]);
	}
	T query(int b, int e) { // query [b, e)
		T ra = unit, rb = unit;
		for (b += n, e += n; b < e; b /= 2, e /= 2) {
			if (b % 2) ra = f(ra, s[b++]);
			if (e % 2) rb = f(s[--e], rb);
		}
		return f(ra, rb);
	}
};

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int n; cin >> n;
    Tree st(n + 1, -1);
    vi v(n);
    for(int & x : v) cin >> x;
    vi w = v;
    sort(all(w));
    w.push_back(1e6 + 1);
    for(int i = 0; i < n; i++) {
        int index = upper_bound(all(w), v[i]) - w.begin() - 1;
        int next_index = index + 1;
        st.update(index, st.query(next_index, n + 1) + 1);
    }
    cout << st.query(0, n) + 1 << '\n';
    return 0;
}